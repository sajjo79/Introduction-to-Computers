# assignment operators give a value to left side variable
x=5                 # simple assignment
print(x)
# two assignment modes
# x+=y  ==>  x=x+y
x=5
x+=3                # same as x=x+3
print(x)

x=5
x=x-3
print(x)

x=5
x*=3
print(x)

x=5
x/=3
print(x)

x=5
x%=3                #remainder operator
print(x)

x=5
x//=3
print(x)




